        function myFunction1() {
          var x = document.getElementById("submission1");
          var y = document.getElementById("results1");
          if (x.style.display === "none") {
            x.style.display = "none";
            y.style.display = "block";
          } else {
            x.style.display = "none";
            y.style.display = "block";
          }
        }

